public class JCountDown {
    public static void main(String[] args) {
//        step 1: declare a variable counter, assign counter=10 and initialise a for loop to iterate
//        until the counter equals 0
       for(int counter=10;counter>0;counter--){
//           step 2: display the value of counter
           System.out.println("counter = " + counter);
       }
    }
}
